    private void Table_moviesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_moviesMouseClicked
        int row= Table_movies.rowAtPoint(evt.getPoint());
        int col= 0;
        String cod_film = Table_movies.getValueAt(row,col).toString();
        String local_cod_personal = Login.cod_personal;
    
        try{
            conn = Connect.ConnectDb();
            cs = conn.prepareCall("begin rezerva_film(?,?); end;");
            cs.setString(1, cod_film);
            cs.setString(2,local_cod_personal);
          
            cs.executeQuery();
            
             Object[] options = {"Da",
                    "Nu"};
            int n = JOptionPane.showOptionDialog(null,
                "Filmul tau a fost rezervat! Doresti sa mai imprumuti un film?",
                "Succes!",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[1]);
            
            if(n==0)
            {}
            if(n==1)
            {
                UserChoice c = new UserChoice();
                this.dispose();
                c.setVisible(true);
            
            }
            
        }catch(SQLException ex){      
            int cod_err = ex.getErrorCode();
            
            if(cod_err == 20900) //nu exista
            {
            JOptionPane.showMessageDialog(null,
            "Filmul nu exista!",
            "Avertisment",
            JOptionPane.WARNING_MESSAGE);
                
            }
           if(cod_err == 20500) //deja imprumutata
            {
                JOptionPane.showMessageDialog(null,
                "Ati mai imprumutat o data aceasta film!",
                "Eroare!",
                JOptionPane.ERROR_MESSAGE);
            
            }
            
        }
    }//GEN-LAST:event_Table_moviesMouseClicked

