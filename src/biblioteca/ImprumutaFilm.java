     private void show_table(){
    
        try{
            conn = Connect.ConnectDb();
            String sql = "select id_film as \"ID Film\", nume as \"Nume Film\", regizor as \"Regizor\", gen as \"Gen\", an_aparitie as \"An Aparitie\", nota_vot as \"Nota IMDB\" from filme";
            pst = (OraclePreparedStatement) conn.prepareStatement(sql);
            rs = (OracleResultSet) pst.executeQuery();
            Table_movies.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){
            e.printStackTrace();
        }
    
    }

