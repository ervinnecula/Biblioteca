    private void show_table(){
    
        try{
            conn = Connect.ConnectDb();
            String sql = "select c.id_carte as \"Id Carte\","
                    + "c.nume as \" Nume Opera\","
            + "a.nume as \"Nume autor\","
            + "c.gen as \"Gen\","
            + "c.editura as \"Editura\","
            + "c.an_aparitie as \"An Aparitie\","
            + "c.limba as \"Limba\" from carti c, autori a where c.cod_autor = a.cod_autor";
            
            pst = (OraclePreparedStatement) conn.prepareStatement(sql);
            rs = (OracleResultSet) pst.executeQuery();
            Table_list_books.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
